print("Python has three numeric types: int, float, and complex")
myValue = 1
print(myValue)
print(type(myValue))

myValueFloat = 3.14
print(myValueFloat)
print(str(myValueFloat) + " is of the data type " + str(type(myValueFloat)))

myValueComplexDataType = 5j
print(myValueComplexDataType)
print(type(myValueComplexDataType))
print(str(myValueComplexDataType) + " is of the data type " +
      str(type(myValueComplexDataType)))

myValueBool = True
print(myValueBool)
print(type(myValueBool))
print(str(myValueBool) + " is the data type " + str(type(myValueBool)))

myValueBoolF = False
print(myValueBoolF)
print(type(myValueBoolF))
print(str(myValueBoolF) + " is the data type " + str(type(myValueBoolF)))
